namespace Sekai.Platform.Windowing;

/// <summary>
/// An enumeration of window border states.
/// </summary>
public enum WindowBorder
{
    /// <summary>
    /// The window cannot be resized.
    /// </summary>
    Fixed,

    /// <summary>
    /// The window is resizable.
    /// </summary>
    Resizable,

    /// <summary>
    /// The window border decoration is hidden.
    /// </summary>
    Hidden,
}
