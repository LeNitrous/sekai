namespace Sekai.Framework.Graphics;

/// <summary>
/// An enumeration of index data types.
/// </summary>
public enum IndexType
{
    /// <summary>
    /// Unsigned 16-bit integer.
    /// </summary>
    UnsignedShort,

    /// <summary>
    /// Unsigned 32-bit integer.
    /// </summary>
    UnsignedInt,

    /// <summary>
    /// Signed 16-bit integer.
    /// </summary>
    Short,

    /// <summary>
    /// Signed 32-bit integer.
    /// </summary>
    Int
}
