namespace Sekai.Input.Devices.Controllers;

/// <summary>
/// A controller button.
/// </summary>
public enum ControllerButton
{
    Unknown = -1,

    /// <summary>
    /// The A button.
    /// </summary>
    A,

    /// <summary>
    /// The B button.
    /// </summary>
    B,

    /// <summary>
    /// The X button.
    /// </summary>
    X,

    /// <summary>
    /// The Y button.
    /// </summary>
    Y,

    /// <summary>
    /// The Left Bumper.
    /// </summary>
    BumperLeft,

    /// <summary>
    /// The Right Bumper.
    /// </summary>
    BumperRight,

    /// <summary>
    /// The Back button.
    /// </summary>
    Back,

    /// <summary>
    /// The Start button.
    /// </summary>
    Start,

    /// <summary>
    /// The Home button.
    /// </summary>
    Home,

    /// <summary>
    /// The Left Stick button.
    /// </summary>
    StickLeft,

    /// <summary>
    /// The Right Stick button.
    /// </summary>
    StickRight,

    /// <summary>
    /// The up directional pad button.
    /// </summary>
    DPadUp,

    /// <summary>
    /// The right directional pad button.
    /// </summary>
    DPadRight,

    /// <summary>
    /// The down directional pad button.
    /// </summary>
    DPadDown,

    /// <summary>
    /// The left directional pad button.
    /// </summary>
    DPadLeft,
}
