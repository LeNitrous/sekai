namespace Sekai.Audio;

/// <summary>
/// Determines the audio format.
/// </summary>
public enum AudioFormat
{
    /// <summary>
    /// 8-bit single channel audio.
    /// </summary>
    Mono8,

    /// <summary>
    /// 16-bit single channel audio.
    /// </summary>
    Mono16,

    /// <summary>
    /// 8-bit dual channel audio.
    /// </summary>
    Stereo8,

    /// <summary>
    /// 16-bit dual channel audio.
    /// </summary>
    Stereo16,
}
