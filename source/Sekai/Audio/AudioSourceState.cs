namespace Sekai.Audio;

/// <summary>
/// The audio source states.
/// </summary>
public enum AudioSourceState
{
    /// <summary>
    /// The controller is fully stopped.
    /// </summary>
    Stopped,

    /// <summary>
    /// The controller is playing.
    /// </summary>
    Playing,

    /// <summary>
    /// The controller is paused.
    /// </summary>
    Paused,
}
