/// <summary>
/// Determines how primitives should be drawn.
/// </summary>
public enum FillMode
{
    /// <summary>
    /// Primitives are completely filled.
    /// </summary>
    Solid,

    /// <summary>
    /// Primitives will be drawn in lines.
    /// </summary>
    Wireframe,
}
