namespace Sekai.Graphics;

/// <summary>
/// An enumeration of texture types.
/// </summary>
public enum TextureType
{
    /// <summary>
    /// One dimensional texture.
    /// </summary>
    Texture1D,

    /// <summary>
    /// Two dimensional texture.
    /// </summary>
    Texture2D,

    /// <summary>
    /// Three dimensional texture.
    /// </summary>
    Texture3D,

    /// <summary>
    /// A cubemap texture.
    /// </summary>
    Cubemap,
}
