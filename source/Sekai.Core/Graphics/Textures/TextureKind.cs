namespace Sekai.Graphics.Textures;

/// <summary>
/// An enumeration of texture kinds.
/// </summary>
public enum TextureKind
{
    /// <summary>
    /// The texture is one-dimensional.
    /// </summary>
    Texture1D,

    /// <summary>
    /// The texture is two-dimensional.
    /// </summary>
    Texture2D,

    /// <summary>
    /// The texture is three-dimensional.
    /// </summary>
    Texture3D,
}
