namespace Sekai.Graphics.Vertices;

/// <summary>
/// An enumeration of vertex member formats.
/// </summary>
public enum LayoutMemberFormat
{
    /// <summary>
    /// Byte.
    /// </summary>
    Byte,

    /// <summary>
    /// Unsigned byte.
    /// </summary>
    UnsignedByte,

    /// <summary>
    /// Short.
    /// </summary>
    Short,

    /// <summary>
    /// Unsigned short.
    /// </summary>
    UnsignedShort,

    /// <summary>
    /// Int.
    /// </summary>
    Int,

    /// <summary>
    /// Unisgned int.
    /// </summary>
    UnsignedInt,

    /// <summary>
    /// Half.
    /// </summary>
    Half,

    /// <summary>
    /// Float.
    /// </summary>
    Float,
}
