namespace Sekai.Framework.Graphics;

/// <summary>
/// An enumeration of shader constant types.
/// </summary>
public enum ShaderConstantType
{
    /// <summary>
    /// A <see cref="bool"/>.
    /// </summary>
    Bool,

    /// <summary>
    /// A <see cref="ushort"/>.
    /// </summary>
    UInt16,

    /// <summary>
    /// A <see cref="short"/>.
    /// </summary>
    Int16,

    /// <summary>
    /// A n<see cref="uint"/>.
    /// </summary>
    UInt32,

    /// <summary>
    /// An <see cref="int"/>.
    /// </summary>
    Int32,

    /// <summary>
    /// A <see cref="ulong"/>.
    /// </summary>
    UInt64,

    /// <summary>
    /// A <see cref="long"/>.
    /// </summary>
    Int64,

    /// <summary>
    /// A <see cref="float"/>.
    /// </summary>
    Float,

    /// <summary>
    /// A <see cref="double"/>.
    /// </summary>
    Double,
}
